package class3;

public class BooleanExample2 {
    public static void main(String[] args) {
        // Explanation: The boolean type represents a logical value that can be either true or false.

        boolean isOK = true; // The boolean variable 'isOK' contains the value true

        boolean hasError = false; // The boolean variable 'hasError' contains the value false

        int age = 70;

        // Explanation: The boolean variable 'isSenior' contains the result of the comparison (age > 65)
        boolean isSenior = (age > 65); // The boolean variable 'isSenior' contains the value true

        int record = 612;
        int value = 615;

        // Explanation: The boolean variable 'hasNewRecord' contains the result of the comparison (value > record)
        boolean hasNewRecord = (value > record); // The boolean variable 'hasNewRecord' contains the value true
    }
}
