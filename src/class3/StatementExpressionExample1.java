package class3;

public class StatementExpressionExample1 {
    public static void main(String[] args) {
        int a; // Statement: Variable declaration

        a = 10; // Expression: Assigning the value 10 to 'a'

        boolean result = (a > 5); // Expression: Comparison using the greater than operator

        int i = 0; // Statement: Variable declaration and initialization

        i++; // Expression: Incrementing the value of 'i' by 1

        int x = 7; // Statement: Variable declaration and initialization
    }
}
